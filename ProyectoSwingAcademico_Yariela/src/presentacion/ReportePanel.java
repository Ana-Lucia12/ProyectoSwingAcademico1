    private void jbtnExportar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnExportar1ActionPerformed
                            
        JFileChooser guardar = new JFileChooser();
        
        String ubicacion = 
        jcboGuardaren.getSelectedItem().toString();

        int opcion = guardar.showSaveDialog(this);

        if(opcion == JFileChooser.APPROVE_OPTION){

            try{

                String tipo = 
                jcboTipoArchivo1.getSelectedItem().toString();

                String nombreArchivo =
                guardar.getSelectedFile().getAbsolutePath();

                if(tipo.equals("TXT")){

                    FileWriter archivo =
                    new FileWriter(nombreArchivo + ".txt");

                    archivo.write(
                    "Reporte de estudiantes\n"
                    +"Cantidad por carrera\n"
                    );

                    archivo.close();

                }else{
                    
                
                    FileWriter archivo =
                    new FileWriter(nombreArchivo + ".csv");

                    archivo.write(
                    "Carrera,Cantidad\n"
                    +"Sistemas,10\n"
                    );

                    archivo.close();
                }

                JOptionPane.showMessageDialog(
                this,
                "Archivo guardado correctamente"
                );

            }catch(Exception e){
                
                JOptionPane.showMessageDialog(
                this,
                "Error al guardar archivo"
                );
            }
        }

    }//GEN-LAST:event_jbtnExportar1ActionPerformed



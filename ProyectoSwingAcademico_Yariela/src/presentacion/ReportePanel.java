    private void jButtonExpReporte1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExpReporte1ActionPerformed
         JFileChooser guardar = new JFileChooser();

        int opcion = guardar.showSaveDialog(this);


        if(opcion == JFileChooser.APPROVE_OPTION){

            try{

                FileWriter archivo = new FileWriter(
                    guardar.getSelectedFile()+".txt"
                );


                archivo.write(
                    "Reporte de estudiantes\n"
                    + "Carrera: Sistemas\n"
                    + "Cantidad: 10\n"
                );


                archivo.close();


                JOptionPane.showMessageDialog(
                    this,
                    "Archivo guardado correctamente"
                );


            }catch(Exception e){

                JOptionPane.showMessageDialog(
                    this,
                    "Error al guardar"
                );

            }
        
        }
    }//GEN-LAST:event_jButtonExpReporte1ActionPerformed


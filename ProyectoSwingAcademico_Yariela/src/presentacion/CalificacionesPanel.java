    private void cargarTabla() {

        DefaultTableModel modelo =
                (DefaultTableModel) jTableCalificaciones.getModel();

        modelo.setRowCount(0);
        
        List<Calificacion> lista =
            negocio.obtenerTodas();

        for (Calificacion c : lista) {

            String estado;

            if (c.getNota() >= 70) {
                estado = "Aprobado";
            } else {
                estado = "Reprobado";
            }

            modelo.addRow(new Object[]{
                c.getNota(),
                estado
            });
        }
    }
    
    private void cargarCombos() {

    jcboEstudiante.removeAllItems();
    jcboCurso.removeAllItems();

    for (Matricula m : matriculaNegocio.listar()) {

        if (m.getEstudiante() != null) {

            jcboEstudiante.addItem(
                m.getEstudiante().getNombre()
            );
        }

        if (m.getCurso() != null) {

            jcboCurso.addItem(
                m.getCurso().getNombre()
            );
        }
    }
    }
    

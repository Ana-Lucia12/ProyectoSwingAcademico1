    private void jbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnGuardarActionPerformed
        try {

            if (jtxtNota.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(
                        this,
                        "Debe ingresar una nota");
                return;
            }

            double nota;

            try {

                nota = Double.parseDouble(
                        jtxtNota.getText());

            } catch (NumberFormatException ex) {

                JOptionPane.showMessageDialog(
                        this,
                        "La nota debe ser numérica");

                return;
            }

            if (nota < 0 || nota > 100) {

                JOptionPane.showMessageDialog(
                        this,
                        "La nota debe estar entre 0 y 100");

                return;
            }

            String observacion =
                    jtxtObservacion.getText();

            Calificacion c =
                    new Calificacion();

            c.setNota(nota);

            negocio.agregar(c);
           

            JOptionPane.showMessageDialog(
                    this,
                    "Nota guardada correctamente");
            
            cargarTabla();
            
            jtxtNota.setText("");
            jtxtObservacion.setText("");
            

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage());
        } 
    }//GEN-LAST:event_jbtnGuardarActionPerformed


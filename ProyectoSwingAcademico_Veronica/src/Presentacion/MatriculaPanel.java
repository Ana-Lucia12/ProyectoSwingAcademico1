    private void jBttnMatricularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBttnMatricularActionPerformed
        Estudiante est = (Estudiante) cmbEstudiante.getSelectedItem();

        Curso cur = (Curso) cmbCurso.getSelectedItem();

        if(est == null || cur == null){
            JOptionPane.showMessageDialog(this,
                "Seleccione estudiante y curso");
        return;
        }

        DefaultTableModel modelo = (DefaultTableModel) tblMatriculas.getModel();

         String fecha = java.time.LocalDate.now().toString();

        modelo.addRow(new Object[]{
            contadorMatricula,
            est.getNombre(),
            cur.getNombre(),
            fecha,
            "Activa"
         });

        contadorMatricula++;

        JOptionPane.showMessageDialog(this,
            "Matrícula registrada");
    }//GEN-LAST:event_jBttnMatricularActionPerformed

    private void jBttnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBttnLimpiarActionPerformed
        cmbEstudiante.setSelectedIndex(-1);
        cmbCurso.setSelectedIndex(-1);

        lblCarrera.setText("");
        lblEstado.setText("");
        lblCreditos.setText("");
        lblProfesor.setText("");
    }//GEN-LAST:event_jBttnLimpiarActionPerformed


    private void jBttnEliminarMatriculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBttnEliminarMatriculaActionPerformed
        int fila = tblMatriculas.getSelectedRow();

        if(fila == -1){

        JOptionPane.showMessageDialog(this,
                "Seleccione una fila");

        return;
        }

        DefaultTableModel modelo =
            (DefaultTableModel) tblMatriculas.getModel();

        modelo.removeRow(fila);

        JOptionPane.showMessageDialog(this,
            "Matrícula eliminada");
    }//GEN-LAST:event_jBttnEliminarMatriculaActionPerformed

